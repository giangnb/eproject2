    private void initText() {
        emailTitle = "";
        switch (type) {
            case CONFIRM:
                lblTitle.setText("Account confirmation");
                lblInfo.setText("To complete account creation,");
                lblInfo1.setText("please check your provided email to get confirmation code.");
                emailTitle = "Account register confirmation - MyBudget";
                emailMessage.append("Hello user,<br/>To validate your email address, please enter ")
                        .append("the folowing <b>Confirmation code<b/> to complete account regisration.")
                        .append("<br/><br/>Here's your code:<br/><h2>").append(code).append("</h2>")
                        .append("<br/><br/>If you didn't register MyBudget account or acquire confirmation code, ")
                        .append("please ignore this email.<br/><i>Thank you!</i>");
                break;
            case RECOVERY:
                lblTitle.setText("Password recovery");
                lblInfo.setText("An email with confirmation code has been sent to ");
                lblInfo1.setText("your account email, please check your email for code.");
                emailTitle = "Password recovery - MyBudget";
                emailMessage.append("Hello user,<br/>To reset your password, please enter ")
                        .append("the folowing <b>Confirmation code<b/> to get your new password.")
                        .append("<br/><br/>Here's your code:<br/><h2>").append(code).append("</h2>")
                        .append("<br/><br/>If you didn't acquire confirmation code, ")
                        .append("please ignore this email.<br/><i>Thank you!</i>");
                break;
        }
        new Mailing(email, emailTitle, emailMessage.toString()).start();

        //resetCountdown();
        btnResend.setVisible(false);
    }

    private void resetCountdown() {
        btnResend.setEnabled(false);
        for (int i = 120; i > 0; i--) {
            btnResend.setText("please wait " + i + "s");
            try {
                Thread.sleep(1000);
            } catch (InterruptedException ex) {
                // ignore
            }
        }
        btnResend.setText("Resend code");
        btnResend.setEnabled(true);
    }

