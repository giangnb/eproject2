    private void btnConfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmActionPerformed
        if (txtCode.getText().toUpperCase().equals(code.toUpperCase())) {
            switch (type) {
                case CONFIRM:
                    Account.Info info = App.ACCOUNT.getAccount().getInfo();
                    info.status = 1;
                    App.ACCOUNT.getAccount().setInfo(info);
                     {
                        try {
                            App.ACCOUNT.updateInfo();
                            JOptionPane.showMessageDialog(null, "You have confirmed your account.\nNow you can access MyBudget, enjoy!", "Confirmation completed", JOptionPane.INFORMATION_MESSAGE);
                            MainScreen.run();
                            this.dispose();
                        } catch (AppException ex) {
                            ExceptionViewer.view(ex);
                        }
                    }
                    break;
                case RECOVERY: {
                    try {
                        App.ACCOUNT.recoverPassword(email);
                        JOptionPane.showMessageDialog(null, "Your new password:\n 12345678", "New password", JOptionPane.INFORMATION_MESSAGE);
                        AccountLogin.run();
                        this.dispose();
                    } catch (AppException ex) {
                        ExceptionViewer.view(ex);
                    }
                }
                break;
            }
        } else {
            JOptionPane.showMessageDialog(null, "Wrong confirmation code. Please check again!", "Confiramtion failed", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnConfirmActionPerformed

